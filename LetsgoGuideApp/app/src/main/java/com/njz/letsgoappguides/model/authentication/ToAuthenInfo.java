public class ToAuthenInfo {

    private String name;
    private String idCardNum;
    private int gender;
    private String birthTime;
    private String cardPhoto;
    private String cardPhotoAbove;
    private String cardPhotoBelow;
    private int type;



    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIdCardNum() {
        return idCardNum;
    }

    public void setIdCardNum(String idCardNum) {
        this.idCardNum = idCardNum;
    }

    public int getGender() {
        return gender;
    }

    public void setGender(int gender) {
        this.gender = gender;
    }

    public String getBirthTime() {
        return birthTime;
    }

    public void setBirthTime(String birthTime) {
        this.birthTime = birthTime;
    }

    public String getCardPhoto() {
        return cardPhoto;
    }

    public void setCardPhoto(String cardPhoto) {
        this.cardPhoto = cardPhoto;
    }

    public String getCardPhotoAbove() {
        return cardPhotoAbove;
    }

    public void setCardPhotoAbove(String cardPhotoAbove) {
        this.cardPhotoAbove = cardPhotoAbove;
    }

    public String getCardPhotoBelow() {
        return cardPhotoBelow;
    }

    public void setCardPhotoBelow(String cardPhotoBelow) {
        this.cardPhotoBelow = cardPhotoBelow;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    @Override
    public String toString() {
        return "ToAuthenInfo{" +
                "name='" + name + '\'' +
                ", idCardNum='" + idCardNum + '\'' +
                ", gender=" + gender +
                ", birthTime='" + birthTime + '\'' +
                ", cardPhoto='" + cardPhoto + '\'' +
                ", cardPhotoAbove='" + cardPhotoAbove + '\'' +
                ", cardPhotoBelow='" + cardPhotoBelow + '\'' +
                ", type=" + type +
                '}';
    }
}
